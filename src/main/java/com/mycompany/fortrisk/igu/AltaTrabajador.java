    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String numDocumento = txtNumDocumento.getText();
        String departamento = txtDepartamento.getText();
        String cargo = txtCargo.getText();
        
        control.guardarEmpleado (nombre, apellido, numDocumento, departamento, cargo);

        //Panel informativo de guardado
        JOptionPane optionPane = new JOptionPane ("Se guardÃ³ correctamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("GuardadoExitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);

        //Resetear valores despuÃ©s del guardado
        txtNombre.setText("");
        txtApellido.setText("");
        txtNumDocumento.setText("");
        txtDepartamento.setText("");
        txtCargo.setText("");
    }//GEN-LAST:event_btnGuardarActionPerformed

