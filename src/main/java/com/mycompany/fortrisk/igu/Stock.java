    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tablaEpp.getRowCount() > 0) {
            //Control (se ha seleccionado 1 item)
            if (tablaEpp.getSelectedRow() != -1) {
                //obtener la Id de la EPP a editar
                int referencia = Integer.parseInt(String.valueOf(tablaEpp.getValueAt(tablaEpp.getSelectedRow(), 0)));
                
                EditarEpp pantalla = new EditarEpp(referencia);
                pantalla.setVisible(true);
                pantalla.setLocationRelativeTo(null);
                
                this.dispose();
                
            } else {
                mostrarMensaje("No Selecionó ningun registro", "Error", "Error al eliminar");
            }
        } else {
            mostrarMensaje("El registro esta vacio", "Error", "Error al eliminar");
        }
        
        cargarTabla();
    }//GEN-LAST:event_btnEditarActionPerformed

