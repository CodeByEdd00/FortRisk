    private void cargarTabla() {
        //Definir modelo de tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {

            //hacer tabla no editable
            @Override
            public boolean isCellEditable(int row, int colum) {
                return false;
            }
        };

        //Establecer nombres columnas
        String titulos[] = {"referencia", "equipo", "proveedor", "fechaCompra", "Talla", "Observaciones"};
        modeloTabla.setColumnIdentifiers(titulos);

        //Carga de datos desde la BD
        List<EquipoProteccion> listaEpp = control.traerDatos();

        //recorrer lista y mostrar elementos
        if (listaEpp != null) {
            for (EquipoProteccion epp : listaEpp) {
                Object[] objeto = {epp.getReferencia(), epp.getEquipo(), epp.getProveedor(), epp.getFechaCompra(),
                    epp.getTalla(), epp.getObservaciones()};

                //ageregar fila en la tablacc
                modeloTabla.addRow(objeto);
            }
        }
        
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(modeloTabla);
        tablaEpp.setRowSorter(sorter);

        //Asignar el modelado de tabla a la tabla de la IGU
        tablaEpp.setModel(modeloTabla);
    }

    public void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);

    }
